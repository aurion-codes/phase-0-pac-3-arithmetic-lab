
function add(a,b) {
    return (a+b); 
}

function subtract(a,b) {
    return (a-b);
}

function multiply(a,b) {
    return (a*b);
}

function divide(a,b)    {
    return (a/b);
}

function increment(n){
    n++
    return (n);
}

function decrement(n){
    n--
    return(n);
}

function makeInt(n) {
    return parseInt(n, 10);
}


function preserveDecimal(n){
    parseFloat('2.222', 10)
    return parseFloat(n);
}

